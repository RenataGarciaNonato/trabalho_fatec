
package DTO;


public class FuncionarioDTO {
    private String nome_funcionario, profissao_funcionario;
    private int id_funcionario;

    public String getNome_funcionario() {
        return nome_funcionario;
    }

    public void setNome_funcionario(String nome_funcionario) {
        this.nome_funcionario = nome_funcionario;
    }

    public String getProfissao_funcionario() {
        return profissao_funcionario;
    }

    public void setProfissao_funcionario(String profissao_funcionario) {
        this.profissao_funcionario = profissao_funcionario;
    }

    public int getId_funcionario() {
        return id_funcionario;
    }

    public void setId_funcionario(int id_funcionario) {
        this.id_funcionario = id_funcionario;
    }
    
    
}
